package com.taobao.arthas.core.shell.system;

/**
 * 任务监听器接口
 * <p>
 * 定义了任务生命周期事件的监听接口。
 * 通过实现此接口，可以监听并响应任务的各种状态变化事件，
 * 如任务切换到前台、切换到后台、终止或挂起等。
 * </p>
 * <p>
 * 主要用途：
 * <ul>
 * <li>监听任务在前台和后台之间的切换</li>
 * <li>响应任务的终止事件</li>
 * <li>处理任务的挂起状态</li>
 * <li>实现任务状态的自定义处理逻辑</li>
 * </ul>
 * </p>
 *
 * @author gongdewei 2020-03-23
 */
public interface JobListener {

    /**
     * 任务进入前台事件
     * <p>
     * 当任务从前台切换到前台时调用。
     * 前台任务是指正在与用户直接交互的任务，拥有终端的控制权。
     * </p>
     * <p>
     * 在此方法中可以执行任务切换到前台时需要的操作，
     * 如更新UI显示、恢复任务执行等。
     * </p>
     *
     * @param job 切换到前台的的任务对象
     */
    void onForeground(Job job);

    /**
     * 任务进入后台事件
     * <p>
     * 当任务从前台切换到后台时调用。
     * 后台任务是指在后台运行但不直接与用户交互的任务。
     * </p>
     * <p>
     * 在此方法中可以执行任务切换到后台时需要的操作，
     * 如释放终端控制权、保存任务状态等。
     * </p>
     *
     * @param job 切换到后台的任务对象
     */
    void onBackground(Job job);

    /**
     * 任务终止事件
     * <p>
     * 当任务被终止或执行完成时调用。
     * 任务终止可能是由于正常完成、用户中断或错误导致的异常终止。
     * </p>
     * <p>
     * 在此方法中可以执行任务终止后的清理操作，
     * 如释放资源、记录日志、通知其他组件等。
     * </p>
     *
     * @param job 已终止的任务对象
     */
    void onTerminated(Job job);

    /**
     * 任务挂起事件
     * <p>
     * 当任务被挂起时调用。
     * 挂起状态是指任务暂时停止执行，但还未被终止，稍后可以恢复执行。
     * </p>
     * <p>
     * 在此方法中可以执行任务挂起时的处理操作，
     * 如保存当前执行状态、暂停某些活动等。
     * </p>
     *
     * @param job 被挂起的任务对象
     */
    void onSuspend(Job job);
}
