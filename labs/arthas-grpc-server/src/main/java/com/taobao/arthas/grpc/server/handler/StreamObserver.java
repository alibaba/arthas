package com.taobao.arthas.grpc.server.handler;

/**
 * 流观察者接口
 * <p>
 * 该接口定义了流式通信中的观察者模式，用于处理流式数据的通知。
 * 在 gRPC 中，流观察者用于接收流式调用中的消息通知。
 * </p>
 * <p>
 * 应用场景：
 * <ul>
 *   <li><b>服务端流（Server Streaming）</b>：服务端通过此接口多次发送响应给客户端</li>
 *   <li><b>客户端流（Client Streaming）</b>：客户端通过此接口多次发送请求给服务端</li>
 *   <li><b>双向流（Bidi Streaming）</b>：双方都可以通过此接口多次发送数据</li>
 * </ul>
 * </p>
 * <p>
 * 接口方法调用顺序：
 * <ol>
 *   <li>onNext() 可以被调用零次或多次，用于传递数据</li>
 *   <li>onCompleted() 在最后调用一次，表示流正常结束</li>
 *   <li>如果发生错误，通常会调用 onError() 方法（本接口未定义）</li>
 * </ol>
 * </p>
 *
 * @param <V> 流中传递的数据类型
 * @author FengYe
 * @date 2024/10/24 00:22
 */
public interface StreamObserver<V>  {

    /**
     * 接收流中的下一个消息
     * <p>
     * 当有新数据到达时调用此方法。在流式调用中，此方法可能会被调用多次，
     * 每次调用传递一个数据项。
     * </p>
     * <p>
     * 注意事项：
     * <ul>
     *   <li>此方法可能被并发调用，实现时需要考虑线程安全</li>
     *   <li>在 onCompleted() 调用后，不应再调用此方法</li>
     *   <li>实现时应该快速处理，避免阻塞调用者</li>
     * </ul>
     * </p>
     *
     * @param req 流中的下一个消息对象，类型由泛型参数 V 指定
     */
    void onNext(V req);

    /**
     * 流正常完成时调用
     * <p>
     * 当流正常结束时（没有错误发生），调用此方法通知观察者。
     * 这是流生命周期中最后一个调用的方法。
     * </p>
     * <p>
     * 调用时机：
     * <ul>
     *   <li>服务端流：服务端发送完所有数据后调用</li>
     *   <li>客户端流：客户端发送完所有数据后调用</li>
     *   <li>双向流：双方都表示结束后调用</li>
     * </ul>
     * </p>
     * <p>
     * 注意事项：
     * <ul>
     *   <li>此方法只会被调用一次</li>
     *   <li>此方法调用后，不应再调用 onNext()</li>
     *   <li>可以在此时进行资源清理工作</li>
     * </ul>
     * </p>
     */
    void onCompleted();
}
